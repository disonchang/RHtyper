def complement(s, reverse=False):
    '''
       complement nucleotide
    '''
    s=reverse(s) if reverse else s
    nucl={'A':'T', 'C':'G', 'G':'C', 'T':'A', '-':'-'}
    com=''
    for c in s:
        if c in list(nucl.keys()):
           com+=nucl[c]
        else:
           com+='.'
    return com

def reverse(s):
    s=s[::-1] if reverse else s

